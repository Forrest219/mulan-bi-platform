_mcp_session_states: Dict[str, _MCPSessionState] = {}
_mcp_session_states_lock = threading.Lock()

# 向后兼容别名：原有模块级单例，指向默认站点状态（懒创建）
# 内部函数通过 _get_or_create_session_state 获取正确实例
_mcp_session_state = _MCPSessionState()  # 保留用于向后兼容（_invalidate_session 等）


def _get_site_key(conn) -> str:
    """返回站点唯一键：{server_url}|{site}"""
    server_url = getattr(conn, "server_url", "") or ""
    site = getattr(conn, "site", "") or ""
    return f"{server_url}|{site}"


def _load_active_tableau_mcp_binding(connection_id: Optional[int]) -> Optional[dict]:
    if not isinstance(connection_id, int):
        return None
    try:
        from app.core.database import SessionLocal
        from services.mcp.models import McpServer

        session = SessionLocal()
        try:
            binding = (
                session.query(McpServer)
                .filter(
                    McpServer.type == "tableau",
                    McpServer.tableau_connection_id == connection_id,
                    McpServer.is_active == True,
                )
                .order_by(McpServer.updated_at.desc(), McpServer.id.desc())
                .first()
            )
            if not binding:
                return None
            return {
                "mcp_server_id": binding.id,
                "mcp_server_url": binding.server_url,
                "mcp_binding_status": binding.binding_status,
            }
        finally:
            session.close()
    except Exception as exc:
        logger.warning("Failed to load Tableau MCP binding for connection_id=%s: %s", connection_id, exc)
        return None


def _get_effective_mcp_base_url(conn) -> Optional[str]:
    """Return a configured MCP endpoint, ignoring accidental Tableau server URLs.

    Some records were backfilled with mcp_server_url equal to server_url
    (for example https://bi.ksyun.com). That is a Tableau site root, not an MCP
    streamable-http endpoint, and initialize returns 200 without mcp-session-id.
    In that case fall back to TABLEAU_MCP_SERVER_URL.
    """
    binding = _load_active_tableau_mcp_binding(getattr(conn, "id", None))
    if binding and binding.get("mcp_server_url"):
        return str(binding["mcp_server_url"]).strip().rstrip("/")

    raw_mcp_url = getattr(conn, "mcp_server_url", None)
    raw_server_url = getattr(conn, "server_url", None)
    mcp_url = raw_mcp_url.strip() if isinstance(raw_mcp_url, str) else ""
    server_url = raw_server_url.strip() if isinstance(raw_server_url, str) else ""
    if not mcp_url:
        return None
    if server_url and mcp_url.rstrip("/") == server_url.rstrip("/"):
        logger.warning(
            "Ignoring mcp_server_url equal to Tableau server_url; falling back to TABLEAU_MCP_SERVER_URL: %s",
            mcp_url,
        )
        return None
    return mcp_url


def _get_or_create_session_state(site_key: str, _max_sites: int = 50) -> _MCPSessionState:
    """
    获取或创建指定 site_key 的 _MCPSessionState，并实现 LRU 驱逐（P1）。

    参数：
        site_key: 由 _get_site_key(conn) 返回的字符串
        _max_sites: 最大站点数，达到上限时驱逐最久未访问的站点（默认 50）

    线程安全：全部操作在 _mcp_session_states_lock 内进行。

    严格上限强制（Gap 4）：无论 site_key 是否已存在，创建新 entry 前必须检查。
    若超出上限则先驱逐最旧 session 再创建。
    """
    with _mcp_session_states_lock:
        now = time.monotonic()

        # 严格上限检查：当 site_key 不在字典中且池已满时，立即驱逐最旧 session
        if site_key not in _mcp_session_states and len(_mcp_session_states) >= _max_sites:
            oldest_key = min(
                _mcp_session_states.keys(),
                key=lambda k: _mcp_session_states[k].last_used
            )
            oldest_state = _mcp_session_states.pop(oldest_key)
            logger.info(
                "MCP session pool: evicted %s (last_used=%.1fs ago, pool_size=%d)",
                oldest_key,
                now - oldest_state.last_used,
                len(_mcp_session_states) + 1,
            )
            # 释放 MCP session（非阻塞，驱逐过程中不持有 oldest_state.lock）
            try:
                _invalidate_session(session_state=oldest_state)
            except Exception as e:
                logger.warning("MCP session pool evict warning: %s", e)

        if site_key not in _mcp_session_states:
            _mcp_session_states[site_key] = _MCPSessionState()
            logger.debug("_get_or_create_session_state: 创建新 session_state, site_key=%s", site_key)

        state = _mcp_session_states[site_key]
        state.last_used = now  # 每次访问更新 LRU 时间戳
        return state


