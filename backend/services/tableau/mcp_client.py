def _extract_text(content: list) -> str:
    """从 MCP content[] 数组中提取 text 类型的值（拼接，兼容多段）。"""
    parts = [c.get("text", "") for c in content if c.get("type") == "text"]
    return "".join(parts)


def _parse_tool_json_text(body: dict, tool_name: str) -> Any:
    """Parse a generic MCP tools/call JSON text payload."""
    if "error" in body:
        err = body["error"]
        message = err.get("message", "MCP 返回错误")
        raise TableauMCPError(
            code=_map_mcp_error(err.get("code"), message=message),
            message=message,
            details=err.get("data", {}),
        )

    result = body.get("result", {})
    if result.get("isError"):
        error_text = _extract_text(result.get("content", []))
        raise TableauMCPError(
            code="NLQ_006",
            message=f"MCP 工具执行失败: {error_text[:200]}",
            details={"tool": tool_name, "raw": error_text[:500]},
        )

    text_payload = _extract_text(result.get("content", []))
    try:
        return json.loads(text_payload)
    except json.JSONDecodeError as e:
        raise TableauMCPError(
            code="NLQ_006",
            message=f"{tool_name} 返回非 JSON 文本",
            details={"raw": text_payload[:500], "json_error": str(e)},
        )


def _parse_tools_list_response(body: dict) -> list[Dict[str, Any]]:
    """Parse standard MCP tools/list response without inferring tool semantics."""
    if "error" in body:
        err = body["error"]
        message = err.get("message", "MCP 返回错误")
        raise TableauMCPError(
            code=_map_mcp_error(err.get("code"), message=message),
            message=message,
            details=err.get("data", {}),
        )

    result = body.get("result", body)
    tools = result.get("tools") if isinstance(result, dict) else None
    if not isinstance(tools, list):
        raise TableauMCPError(
            code="NLQ_006",
            message="MCP tools/list 返回值缺少 tools 数组",
            details={"raw": body},
        )

    normalized_tools: list[Dict[str, Any]] = []
    for tool in tools:
        if isinstance(tool, dict):
            name = str(tool.get("name") or "").strip()
            if name:
                normalized_tools.append(tool)
        elif isinstance(tool, str):
            name = tool.strip()
            if name:
                normalized_tools.append({"name": name})
    return normalized_tools


def _mcp_tool_name(tool: Any) -> str:
    if isinstance(tool, dict):
        return str(tool.get("name") or "").strip()
    return str(tool or "").strip()


def _allowed_nl_query_tool_names(
    allowed_tool_names: Optional[Iterable[str]] = None,
) -> tuple[str, ...]:
    if isinstance(allowed_tool_names, str):
        raw_names = (allowed_tool_names,)
    else:
        raw_names = (
            allowed_tool_names
            if allowed_tool_names is not None
            else _DEFAULT_NL_QUERY_TOOL_NAMES
        )
    names: list[str] = []
    seen: set[str] = set()
    for raw_name in raw_names:
        name = str(raw_name or "").strip()
        if not name or name in seen:
            continue
        names.append(name)
        seen.add(name)
    return tuple(names)


def _select_nl_query_tool(
    tools: list[Dict[str, Any]],
    *,
    allowed_tool_names: Optional[Iterable[str]] = None,
) -> Optional[Dict[str, Any]]:
    """Select an exact allowlisted NL tool name; never select structured query-datasource."""
    tools_by_name = {_mcp_tool_name(tool): tool for tool in tools}
    for tool_name in _allowed_nl_query_tool_names(allowed_tool_names):
        if tool_name in _STRUCTURED_QUERY_DATASOURCE_TOOL_NAMES:
            continue
        tool = tools_by_name.get(tool_name)
        if tool is not None:
            return tool
    return None


def _build_nl_query_tool_arguments(
    *,
    tool: Dict[str, Any],
    datasource_luid: str,
    question: str,
    limit: int,
) -> Dict[str, Any]:
    """Build only passthrough NL arguments from the tool schema when available."""
    properties = _tool_schema_properties(tool)
    question_key = _pick_schema_property(properties, _NL_QUESTION_ARGUMENT_NAMES)
    datasource_key = _pick_schema_property(properties, _NL_DATASOURCE_ARGUMENT_NAMES)
    if properties and (question_key is None or datasource_key is None):
        raise TableauMCPError(
            code=MCP_NL_TOOL_UNAVAILABLE,
            message="已配置的 MCP 自然语言查询工具缺少问题或数据源入参",
            details={
                "tool": _mcp_tool_name(tool),
                "schema_properties": sorted(properties.keys()),
            },
        )

    arguments = {
        question_key or _NL_QUESTION_ARGUMENT_NAMES[0]: question,
        datasource_key or _NL_DATASOURCE_ARGUMENT_NAMES[0]: datasource_luid,
    }

    limit_key = _pick_schema_property(properties, _NL_LIMIT_ARGUMENT_NAMES)
    if limit_key is not None:
        arguments[limit_key] = limit
    return arguments


def _tool_schema_properties(tool: Dict[str, Any]) -> Dict[str, Any]:
    schema = tool.get("inputSchema") or tool.get("input_schema") or {}
    if not isinstance(schema, dict):
        return {}
    properties = schema.get("properties") or {}
    return properties if isinstance(properties, dict) else {}


def _pick_schema_property(
    properties: Dict[str, Any],
    candidates: tuple[str, ...],
) -> Optional[str]:
    if not properties:
        return None
    for candidate in candidates:
        if candidate in properties:
            return candidate
    properties_by_lower = {key.lower(): key for key in properties}
    for candidate in candidates:
        matched = properties_by_lower.get(candidate.lower())
        if matched:
            return matched
    return None


def _normalize_query_datasource_result(data: Dict[str, Any]) -> Dict[str, Any]:
    """
    Normalize official Tableau MCP query-datasource results.

    Current @tableau/mcp-server returns {"data": [{"field": value, ...}]} while
    older Mulan code expects {"fields": [...], "rows": [[...]]}. Preserve the
    original data array and add the tabular shape for downstream renderers.
    """
    if "rows" in data or "fields" in data:
        return data

    records = data.get("data")
    if not isinstance(records, list):
        return data

    if not records:
        return {**data, "fields": [], "rows": []}

    if not all(isinstance(record, dict) for record in records):
        return data

    fields = list(records[0].keys())
    rows = [[record.get(field) for field in fields] for record in records]
    return {**data, "fields": fields, "rows": rows}


def _map_mcp_error(code: Any, message: str = "") -> str:
    """
    将 MCP 错误码映射为 NLQ 系列错误码。

    规则：
        - Tableau/LLM 相关错误 → NLQ_006
        - 超时 → NLQ_007
        - 权限/不存在 → NLQ_009
        - 其他 → NLQ_006
    """
    message_lower = message.lower()
    if "timeout" in message_lower or "timed out" in message_lower or "超时" in message:
        return "NLQ_007"
    if code is None:
        return "NLQ_006"
    code_str = str(code)
    # 常见 VizQL 错误码
    if code_str in ("forbidden", "unauthorized") or code == 403:
        return "NLQ_009"
    if code_str in ("not_found", "notexists") or code == 404:
        return "NLQ_009"
    # Tableau VizQL 特定错误
    if code_str.startswith("VIZQL_") or code_str.startswith("VIZQL-"):
        return "NLQ_006"
    return "NLQ_006"


def _retry_kind_for_mcp_error(exc: TableauMCPError) -> Optional[str]:
    """Classify retryable MCP failures without retrying business/schema errors."""
    text = _mcp_error_summary(exc).lower()
    if any(
        marker in text
        for marker in (
            "invalid arguments",
            "invalid_argument",
            "input validation error",
            "unauthorized",
            "forbidden",
            "permission",
            "权限",
            "认证",
            "字段不存在",
            "unknown field",
        )
    ):
        return None

    detail_kind = str((exc.details or {}).get("retry_kind") or "").strip().lower()
    if detail_kind in {"transport", "read_timeout"}:
        return detail_kind

    if any(
        marker in text
        for marker in (
            "econnreset",
            "econnrefused",
            "connection reset",
            "connection refused",
            "socket hang up",
            "remote end closed",
            "temporarily unavailable",
            "service unavailable",
        )
    ):
        return "transport"

    if exc.code == "NLQ_007" or any(marker in text for marker in ("read timeout", "read timed out", "查询超时")):
        return "read_timeout"

    return None


def _max_attempts_for_retry_kind(retry_kind: Optional[str]) -> int:
    if retry_kind == "transport":
        return _TRANSPORT_RETRY_MAX_ATTEMPTS
    if retry_kind == "read_timeout":
        return _READ_TIMEOUT_RETRY_MAX_ATTEMPTS
    return 1


def _mcp_error_summary(exc: TableauMCPError) -> str:
    details = exc.details or {}
    raw = details.get("raw") or details.get("error") or ""
    return f"{exc.code} {exc.message} {raw}"[:500]


