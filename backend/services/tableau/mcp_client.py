def extract_datasource_metadata_fields(raw: Any) -> list[Dict[str, Any]]:
    """Extract Tableau MCP datasource fields from all observed response shapes."""
    if not isinstance(raw, dict):
        return []

    candidates: list[Any] = []
    candidates.append(raw.get("fields"))

    nested_raw = raw.get("raw")
    if isinstance(nested_raw, dict):
        candidates.append(nested_raw.get("fields"))
        candidates.append(_fields_from_groups(nested_raw.get("fieldGroups")))

    candidates.append(_fields_from_groups(raw.get("fieldGroups")))

    fields: list[Dict[str, Any]] = []
    seen: set[str] = set()
    for candidate in candidates:
        if not isinstance(candidate, list):
            continue
        for item in candidate:
            if not isinstance(item, dict):
                continue
            key = (
                str(item.get("name") or item.get("fieldName") or item.get("fieldCaption") or ""),
                str(item.get("logicalTableId") or item.get("fullyQualifiedName") or ""),
            )
            if key in seen:
                continue
            seen.add(key)
            fields.append(item)
    return fields


def _fields_from_groups(groups: Any) -> list[Dict[str, Any]]:
    if not isinstance(groups, list):
        return []
    fields: list[Dict[str, Any]] = []
    for group in groups:
        if not isinstance(group, dict):
            continue
        group_fields = group.get("fields")
        if not isinstance(group_fields, list):
            continue
        for field in group_fields:
            if isinstance(field, dict):
                fields.append(field)
    return fields


def normalize_datasource_metadata_field(field: Dict[str, Any]) -> Dict[str, Any]:
    """Normalize one Tableau MCP field for DB cache storage."""
    field_name = field.get("fieldName") or field.get("name") or field.get("fieldCaption") or ""
    field_caption = field.get("fieldCaption") or field.get("caption") or ""
    data_type = field.get("dataType") or field.get("data_type") or field.get("type") or "STRING"
    role = field.get("role") or field.get("semanticRole") or "dimension"
    role = str(role).lower()
    aggregation = field.get("aggregation") or field.get("defaultAggregation")
    formula = field.get("formula")
    column_class = str(field.get("columnClass") or "").upper()
    is_calculated = bool(field.get("isCalculated") or field.get("is_calculated") or formula or column_class == "CALCULATION")

    metadata_json = dict(field)
    for key in _MCP_FIELD_METADATA_KEYS:
        metadata_json.setdefault(key, field.get(key))

    return {
        "field_name": str(field_name),
        "field_caption": str(field_caption) if field_caption is not None else "",
        "data_type": str(data_type) if data_type is not None else "STRING",
        "role": role if role in ("measure", "dimension") else role,
        "description": field.get("description") or "",
        "formula": formula,
        "aggregation": aggregation,
        "is_calculated": is_calculated,
        "metadata_json": metadata_json,
    }

