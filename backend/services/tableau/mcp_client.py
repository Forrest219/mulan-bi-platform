    def _query_datasource_with_retry(
        self,
        *,
        payload: dict,
        timeout: int,
        site_key: Optional[str],
        session_state: "_MCPSessionState",
        base_url: str,
        jwt_token: Optional[str],
        datasource_luid: str,
        mcp_context: Optional[Dict[str, Any]],
    ) -> Dict[str, Any]:
        """Execute query-datasource with bounded retry for transient MCP failures."""
        budget = min(float(timeout), _QUERY_RETRY_BUDGET_SECONDS)
        deadline = time.monotonic() + max(1.0, budget)
        attempt = 0
        retry_errors: list[str] = []

        while True:
            attempt += 1
            remaining = deadline - time.monotonic()
            if remaining <= 0:
                raise TableauMCPError(
                    code="NLQ_007",
                    message="MCP 查询超过重试总预算",
                    details={
                        "datasource_luid": datasource_luid,
                        "attempts": attempt - 1,
                        "budget_seconds": budget,
                        "retry_errors": retry_errors[-3:],
                    },
                )
            attempt_timeout = max(1, min(int(timeout), int(remaining)))
            try:
                response = self._send_jsonrpc(
                    payload,
                    timeout=attempt_timeout,
                    site_key=site_key,
                    session_state=session_state,
                    base_url=base_url,
                    jwt_token=jwt_token,
                    mcp_context=mcp_context,
                )
                return self._parse_jsonrpc_response(response)
            except TableauMCPError as exc:
                retry_kind = _retry_kind_for_mcp_error(exc)
                max_attempts = _max_attempts_for_retry_kind(retry_kind)
                if retry_kind is None or attempt >= max_attempts:
                    if retry_errors:
                        exc.details.setdefault("retry_attempts", attempt - 1)
                        exc.details.setdefault("retry_errors", retry_errors[-3:])
                    raise

                backoff = _RETRY_BACKOFF_SECONDS[min(attempt - 1, len(_RETRY_BACKOFF_SECONDS) - 1)]
                remaining_after_backoff = deadline - time.monotonic() - backoff
                if remaining_after_backoff < _MIN_RETRY_REMAINING_SECONDS:
                    exc.details.setdefault("retry_attempts", attempt - 1)
                    exc.details.setdefault("retry_budget_exhausted", True)
                    raise

                error_summary = _mcp_error_summary(exc)
                retry_errors.append(error_summary)
                logger.warning(
                    "Retrying Tableau MCP query-datasource after transient %s error "
                    "(attempt=%s/%s, datasource_luid=%s, remaining=%.2fs): %s",
                    retry_kind,
                    attempt + 1,
                    max_attempts,
                    datasource_luid,
                    max(0.0, deadline - time.monotonic()),
                    error_summary,
                )
                time.sleep(backoff)

