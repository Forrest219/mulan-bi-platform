@router.get("/datasources/{asset_id}/metadata")
async def tableau_v2_metadata(
    asset_id: int,
    request: Request,
    refresh: bool = False,
    db: Session = Depends(get_db),
):
    """V2-2: 获取数据源字段元数据（带缓存）"""
    user = get_current_user(request, db)
    _db = TableauDatabase(session=db)

    asset = _db.get_asset(asset_id)
    if not asset or asset.is_deleted:
        raise HTTPException(status_code=404, detail="资产不存在")
    if asset.asset_type != "datasource":
        raise HTTPException(
            status_code=400,
            detail={"error_code": "MCP_010", "message": "该资产不是 datasource 类型"},
        )

    verify_connection_access(asset.connection_id, user, db)
    conn = _db.get_connection(asset.connection_id)
    if not conn:
        raise HTTPException(status_code=404, detail="连接不存在")

    cached_fields = _db.get_datasource_fields(asset_id)
    now = _time.time()
    cache_ttl = 24 * 3600  # 24h

    def _metadata_response(fields, *, cache_status: str, cached_at: Optional[str]):
        return _fields_response_payload(
            fields,
            cache_status=cache_status,
            source="mcp_cache",
            datasource_luid=asset.tableau_id,
            cached_at=cached_at,
        )

    # 缓存命中且未过期
    if cached_fields and not refresh:
        oldest = min((f.fetched_at.timestamp() for f in cached_fields), default=0)
        if now - oldest < cache_ttl:
            return _metadata_response(
                cached_fields,
                cache_status="cached",
                cached_at=_time.strftime("%Y-%m-%dT%H:%M:%SZ", _time.gmtime(oldest)),
            )

    # 需要拉取（首次 or refresh=true）
    try:
        client = get_tableau_mcp_client(asset.connection_id)
        raw = client.get_datasource_metadata(asset.tableau_id, timeout=30)
        raw_fields = extract_datasource_metadata_fields(raw)

        # 写入缓存
        if raw_fields:
            parsed = [normalize_datasource_metadata_field(f) for f in raw_fields]
            _db.upsert_datasource_fields(asset.id, asset.tableau_id, parsed)
            try:
                from services.tableau.field_reconciliation import TableauFieldReconciliationService

                TableauFieldReconciliationService(db).reconcile_asset(
                    asset_id=asset.id,
                    connection_id=asset.connection_id,
                    datasource_luid=asset.tableau_id,
                    metadata=raw,
                )
            except Exception:
                logger.warning(
                    "Tableau MCP metadata reconciliation skipped after refresh: asset_id=%s datasource_luid=%s",
                    asset.id,
                    asset.tableau_id,
                    exc_info=True,
                )
        else:
            logger.warning(
                "Tableau MCP metadata refresh returned no parseable fields: asset_id=%s datasource_luid=%s raw_keys=%s",
                asset.id,
                asset.tableau_id,
                list(raw.keys()) if isinstance(raw, dict) else type(raw).__name__,
            )
            if cached_fields:
                oldest = min((f.fetched_at.timestamp() for f in cached_fields), default=0)
                return _metadata_response(
                    cached_fields,
                    cache_status="stale",
                    cached_at=_time.strftime("%Y-%m-%dT%H:%M:%SZ", _time.gmtime(oldest)),
                )
            raise HTTPException(
                status_code=502,
                detail={
                    "error_code": "MCP_011",
                    "message": "MCP 返回了元数据但未解析出字段",
                },
            )

        # 重新读取（包含新数据）
        cached_fields = _db.get_datasource_fields(asset_id)
        oldest = min((f.fetched_at.timestamp() for f in cached_fields), default=0)
        return _metadata_response(
            cached_fields,
            cache_status="fresh",
            cached_at=_time.strftime("%Y-%m-%dT%H:%M:%SZ", _time.gmtime(oldest)) if cached_fields else None,
        )

    except TableauMCPError:
        # MCP 不可达，降级返回缓存数据
        if cached_fields:
            oldest = min((f.fetched_at.timestamp() for f in cached_fields), default=0)
            return _metadata_response(
                cached_fields,
                cache_status="stale",
                cached_at=_time.strftime("%Y-%m-%dT%H:%M:%SZ", _time.gmtime(oldest)),
            )
        raise HTTPException(
            status_code=503,
            detail={"error_code": "MCP_003", "message": "MCP Server 不可达，且无缓存数据"},
        )


