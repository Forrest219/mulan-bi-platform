@router.post("/connections/{conn_id}/sync")
async def sync_connection(
    conn_id: int,
    current_user: dict = Depends(require_roles(["admin", "data_admin"])),
    db: Session = Depends(get_db),
):
    """
    Spec 13 §3.4 T2: 触发 Tableau 资产同步（Celery 异步任务）。
    
    degraded 状态下返回 503 Service Unavailable。
    """
    _db = TableauDatabase(session=db)

    verify_connection_access(conn_id, current_user, db)

    conn = _db.get_connection(conn_id)
    if not conn:
        raise HTTPException(status_code=404, detail="连接不存在")

    # Spec 13 §3.4 T2: 检查 MCP 是否 degraded
    mcp_url = getattr(conn, 'mcp_server_url', None) or conn.server_url
    if is_mcp_degraded(mcp_url):
        raise HTTPException(
            status_code=503,
            detail={
                "error_code": "MCP_003",
                "message": "MCP 服务不可用（degraded 状态），同步操作暂停。请等待服务恢复。",
                "mcp_health": get_mcp_health(mcp_url).value,
            },
        )

    if conn.sync_status == "running":
        return {"message": "同步正在进行中", "status": "running"}

    from services.tasks import celery_app
    celery_available = False
    try:
        pong = celery_app.control.inspect(timeout=1.0).ping()
        celery_available = bool(pong)
    except Exception:
        celery_available = False

    _db.set_sync_status(conn_id, "running")

    from datetime import datetime
    from services.tasks.models import BiSyncTask

    sync_task = BiSyncTask(
        schedule_id=None,
        connection_id=conn_id,
        scheduled_at=datetime.now(),
        status="pending",
        trigger_type="manual",
    )
    db.add(sync_task)
    db.commit()
    db.refresh(sync_task)

    if celery_available:
        from services.tasks.tableau_tasks import sync_connection_task
        task = sync_connection_task.apply_async(
            args=[conn_id],
            kwargs={"trigger_type": "manual", "sync_task_id": sync_task.id},
            headers={
                "trigger_type": "manual",
                "triggered_by": current_user["id"],
            },
        )
        log_action(current_user["id"], current_user.get("username", ""), "sync", "tableau_connection", conn_id,
                   after_state={"task_id": task.id, "mode": "celery"})
        return {"task_id": task.id, "message": "同步任务已提交", "status": "pending"}

    # Celery 不可用时回退到同步执行（开发环境兼容）
    import asyncio

    def _run_sync_inline():
        from services.tableau.sync_service import TableauSyncService, TableauRestSyncService
        from services.tableau.models import TableauDatabase, TableauSyncLog, TableauConnection
        from app.core.crypto import get_tableau_crypto
        from app.core.database import get_db_context

        with get_db_context() as sync_db:
            sync_dbw = TableauDatabase(session=sync_db)
            sync_conn = sync_dbw.get_connection(conn_id)
            if not sync_conn:
                return {"status": "error", "message": "连接不存在"}

            sync_log = sync_dbw.create_sync_log(conn_id, trigger_type="manual")
            sync_dbw.set_sync_status(conn_id, "running")

            crypto = get_tableau_crypto()
            try:
                token = crypto.decrypt(sync_conn.token_encrypted)
            except Exception as e:
                msg = f"Token 解密失败: {e}"
                sync_dbw.finish_sync_log(sync_log.id, "failed", error_message=msg)
                sync_dbw.set_sync_status(conn_id, "failed")
                return {"status": "error", "message": msg}

            if getattr(sync_conn, "connection_type", "mcp") == "mcp":
                service = TableauRestSyncService(
                    server_url=sync_conn.server_url, site=sync_conn.site,
                    token_name=sync_conn.token_name, token_value=token,
                    api_version=sync_conn.api_version,
                )
            else:
                service = TableauSyncService(
                    server_url=sync_conn.server_url, site=sync_conn.site,
                    token_name=sync_conn.token_name, token_value=token,
                    api_version=sync_conn.api_version,
                )

            try:
                if not service.connect():
                    msg = "连接 Tableau Server 失败，请检查配置"
                    sync_dbw.finish_sync_log(sync_log.id, "failed", error_message=msg)
                    sync_dbw.set_sync_status(conn_id, "failed")
                    return {"status": "error", "message": msg}

                result = service.sync_all_assets(sync_dbw, conn_id, trigger_type="manual", sync_log_id=sync_log.id)
                sync_dbw.reset_sync_failures(conn_id)
                return {"status": result["status"], "total": result["total"], "sync_log_id": sync_log.id}
            except Exception as e:
                msg = str(e)
                sync_dbw.finish_sync_log(sync_log.id, "failed", error_message=msg)
                sync_dbw.set_sync_status(conn_id, "failed")
                return {"status": "error", "message": msg, "sync_log_id": sync_log.id}
            finally:
                service.disconnect()

    result = await asyncio.to_thread(_run_sync_inline)
    sync_task.status = "failed" if result.get("status") == "error" else "completed"
    sync_task.sync_log_id = result.get("sync_log_id")
    sync_task.error_message = result.get("message") if result.get("status") == "error" else None
    sync_task.updated_at = datetime.now()
    db.commit()
    if result.get("status") == "error":
        raise HTTPException(status_code=500, detail={"message": result["message"]})
    log_action(current_user["id"], current_user.get("username", ""), "sync", "tableau_connection", conn_id)
    return {
        "message": f"同步已完成，共处理 {result.get('total', 0)} 个资产",
        "status": "completed",
        "total": result.get("total", 0),
    }


