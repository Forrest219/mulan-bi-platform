@router.get("/connections")
async def list_connections(request: Request, db: Session = Depends(get_db), include_inactive: bool = False):
    """获取 Tableau 连接列表"""
    user = get_current_user(request, db)
    _db = TableauDatabase(session=db)

    if user["role"] == "admin":
        connections = _db.get_all_connections(include_inactive=include_inactive)
    else:
        connections = _db.get_all_connections(owner_id=user["id"], include_inactive=include_inactive)

    connection_dicts = [c.to_dict(db) for c in connections]

    # 仅在没有真实 tableau_connections 时保留 10000+ 虚拟连接兜底。
    # 正常路径应由 MCP 配置创建/更新时桥接到 tableau_connections。
    try:
        from services.mcp.models import McpServer
        if connection_dicts:
            active_mcp_count = db.query(McpServer).filter(
                McpServer.type == "tableau",
                McpServer.is_active == True,
            ).count()
            if active_mcp_count:
                logger.info(
                    "Skipping %d virtual Tableau MCP connection fallback rows because real tableau_connections exist",
                    active_mcp_count,
                )
        else:
            from app.api.mcp_configs import _normalize_tableau_credentials
            mcp_servers = db.query(McpServer).filter(
                McpServer.type == "tableau",
                McpServer.is_active == True,
            ).all()

            for mcp in mcp_servers:
                try:
                    creds = _normalize_tableau_credentials(mcp.credentials)
                except Exception as exc:
                    logger.warning(
                        "Skipping virtual Tableau MCP fallback for mcp_server id=%s name=%s: credential decrypt failed: %s",
                        mcp.id,
                        mcp.name,
                        exc,
                    )
                    continue
                if not (creds.get("pat_value") and creds.get("tableau_server") and creds.get("pat_name")):
                    logger.warning(
                        "Skipping virtual Tableau MCP fallback for mcp_server id=%s name=%s: incomplete credentials",
                        mcp.id,
                        mcp.name,
                    )
                    continue
                connection_dicts.append({
                    "id": 10000 + mcp.id,
                    "name": mcp.name,
                    "server_url": creds.get("tableau_server") or mcp.server_url or "",
                    "site": creds.get("site_name") or "",
                    "api_version": "3.21",
                    "connection_type": "mcp",
                    "token_name": creds.get("pat_name") or "",
                    "owner_id": None,
                    "is_active": True,
                    "auto_sync_enabled": False,
                    "sync_interval_hours": 24,
                    "last_test_at": None,
                    "last_test_success": None,
                    "last_test_message": "virtual MCP fallback; bridge to tableau_connections pending",
                    "last_sync_at": None,
                    "last_sync_duration_sec": None,
                    "sync_status": "idle",
                    "mcp_direct_enabled": True,
                    "mcp_server_url": mcp.server_url or "",
                    "next_sync_at": None,
                    "created_at": mcp.created_at.strftime("%Y-%m-%d %H:%M:%S") if mcp.created_at else None,
                    "updated_at": mcp.updated_at.strftime("%Y-%m-%d %H:%M:%S") if mcp.updated_at else None,
                    "source": "mcp_virtual_fallback",
                })
    except Exception:
        logger.exception("从 mcp_servers 聚合 Tableau 连接时出错，返回仅 tableau_connections 的结果")

    # 修正 stale sync_status（基于最新 TableauSyncLog）
    # 场景：Celery 任务完成但未更新 connections 表的 sync_status
    # 规则：连接 sync_status 应与最新 log status 对齐
    #   log=running  → 连接保持 running（正常进行中）
    #   log=success  → 连接应为 idle
    #   log=failed/partial → 连接应为 failed
    try:
        running_conn_ids = [c.id for c in connections if c.sync_status == "running"]
        if running_conn_ids:
            updates = {}  # conn_id -> new status
            for conn_id in running_conn_ids:
                max_log = (
                    db.query(sa.func.max(TableauSyncLog.id))
                    .filter(TableauSyncLog.connection_id == conn_id)
                    .scalar()
                )
                if max_log:
                    log = db.query(TableauSyncLog).filter(TableauSyncLog.id == max_log).first()
                    if log and log.status != "running":
                        new_status = "idle" if log.status == "success" else "failed"
                        updates[conn_id] = new_status
                        logger.info(f"[sync_status reconciliation] conn_id={conn_id} log_status={log.status} → {new_status}")
            if updates:
                for conn_id, new_status in updates.items():
                    db.query(TableauConnectionModel).filter(
                        TableauConnectionModel.id == conn_id
                    ).update(
                        {TableauConnectionModel.sync_status: new_status},
                        synchronize_session=False,
                    )
                db.commit()
                # 重新查询更新后的连接
                if user["role"] == "admin":
                    connections = _db.get_all_connections(include_inactive=include_inactive)
                else:
                    connections = _db.get_all_connections(owner_id=user["id"], include_inactive=include_inactive)
                connection_dicts = [c.to_dict(db) for c in connections]
    except Exception:
        logger.exception("修正 stale sync_status 时出错")

    return {"connections": connection_dicts, "total": len(connection_dicts)}


@router.post("/connections", status_code=201)
async def create_connection(
    req: CreateConnectionRequest,
    current_user: dict = Depends(require_roles(["admin", "data_admin"])),
    db: Session = Depends(get_db),
):
    """创建 Tableau 连接"""
    if req.connection_type not in ("mcp", "tsc"):
        raise HTTPException(status_code=400, detail="connection_type 必须为 'mcp' 或 'tsc'")

    duplicate = _find_duplicate_connection(
        db,
        server_url=req.server_url,
        site=req.site,
        owner_id=current_user["id"],
    )
    if duplicate is not None:
        raise HTTPException(
            status_code=409,
            detail={"code": "TAB_409", "message": "该 Tableau 站点已接入，请复用已有连接"},
        )

    if req.connection_type == "mcp":
        test_result = _test_connection_rest(
            req.server_url,
            req.site,
            req.token_name,
            req.token_value,
            req.api_version,
        )
        if not test_result.get("success"):
            raise HTTPException(
                status_code=400,
                detail={"code": "TAB_REST_001", "message": test_result.get("message", "Tableau REST 测试失败")},
            )

    encrypted_token = _encrypt(req.token_value)
    conn = TableauConnectionModel(
        name=req.name,
        server_url=req.server_url,
        site=req.site,
        token_name=req.token_name,
        token_encrypted=encrypted_token,
        owner_id=current_user["id"],
        api_version=req.api_version,
        connection_type=req.connection_type,
        auto_sync_enabled=req.auto_sync_enabled,
        schedule_id=req.schedule_id if req.auto_sync_enabled else None,
    )
    db.add(conn)
    db.flush()

    binding = TableauMcpBindingService(db).upsert_for_connection(
        connection=conn,
        enabled=req.agent_enabled,
        owner_id=current_user["id"],
        health_check=False,
    )
    db.commit()
    binding = _refresh_binding_health_after_commit(
        db,
        connection_id=conn.id,
        user_id=current_user["id"],
        binding=binding,
    )
    db.refresh(conn)

    log_action(current_user["id"], current_user.get("username", ""), "create", "tableau_connection", conn.id,
               after_state={"name": conn.name, "server_url": conn.server_url, "site": conn.site})
    payload = conn.to_dict(db)
    payload["mcp_binding"] = _binding_payload(binding)
    return {"connection": payload, "message": "连接创建成功"}


@router.put("/connections/{conn_id}")
async def update_connection(
    conn_id: int,
    req: UpdateConnectionRequest,
    current_user: dict = Depends(require_roles(["admin", "data_admin"])),
    db: Session = Depends(get_db),
):
    """更新 Tableau 连接"""
    _db = TableauDatabase(session=db)

    # 使用统一的权限验证函数
    verify_connection_access(conn_id, current_user, db)

    current = _db.get_connection(conn_id)
    if not current:
        raise HTTPException(status_code=404, detail="连接不存在")

    update_data = req.model_dump(exclude_unset=True)
    agent_enabled = update_data.pop("agent_enabled", None)
    new_server_url = update_data.get("server_url", current.server_url)
    new_site = update_data.get("site", current.site)
    connection_key_changed = (
        _normalize_connection_key(new_server_url, new_site)
        != _normalize_connection_key(current.server_url, current.site)
    )
    if connection_key_changed:
        duplicate = _find_duplicate_connection(
            db,
            server_url=new_server_url,
            site=new_site,
            owner_id=current.owner_id,
            exclude_id=conn_id,
        )
        if duplicate is not None:
            raise HTTPException(
                status_code=409,
                detail={"code": "TAB_409", "message": "该 Tableau 站点已接入，请复用已有连接"},
            )

    token_for_test = None
    if "token_value" in update_data and update_data["token_value"]:
        # 用户提供了新 token_value，同时更新 token_name 和加密后的 token
        token_for_test = update_data["token_value"]
        update_data["token_encrypted"] = _encrypt(update_data.pop("token_value"))
        if req.token_name:
            update_data["token_name"] = req.token_name
    elif "token_value" in update_data and not update_data["token_value"]:
        # token_value 为空字符串，不更新 token 相关字段
        update_data.pop("token_value", None)
        update_data.pop("token_name", None)

    should_retest = (
        connection_key_changed
        or token_for_test is not None
        or ("api_version" in update_data and update_data["api_version"] != current.api_version)
        or ("token_name" in update_data and update_data["token_name"] != current.token_name)
    )
    if should_retest and current.connection_type == "mcp":
        test_token = token_for_test
        if test_token is None:
            test_token = _decrypt(current.token_encrypted)
        test_result = _test_connection_rest(
            new_server_url,
            new_site,
            update_data.get("token_name", current.token_name),
            test_token,
            update_data.get("api_version", current.api_version),
        )
        if not test_result.get("success"):
            raise HTTPException(
                status_code=400,
                detail={"code": "TAB_REST_001", "message": test_result.get("message", "Tableau REST 测试失败")},
            )

    for key, value in update_data.items():
        if hasattr(current, key) and value is not None:
            setattr(current, key, value)

    if current.is_active is False:
        agent_enabled = False
    if agent_enabled is None:
        agent_enabled = bool(current.mcp_direct_enabled)

    binding = TableauMcpBindingService(db).upsert_for_connection(
        connection=current,
        enabled=bool(agent_enabled),
        owner_id=current_user["id"],
        health_check=False,
    )
    db.commit()
    binding = _refresh_binding_health_after_commit(
        db,
        connection_id=current.id,
        user_id=current_user["id"],
        binding=binding,
    )
    db.refresh(current)

    log_action(current_user["id"], current_user.get("username", ""), "update", "tableau_connection", conn_id)
    payload = current.to_dict(db)
    payload["mcp_binding"] = _binding_payload(binding)
    return {"connection": payload, "message": "连接更新成功"}


@router.delete("/connections/{conn_id}")
async def delete_connection(
    conn_id: int,
    current_user: dict = Depends(require_roles(["admin", "data_admin"])),
    db: Session = Depends(get_db),
):
    """删除 Tableau 连接"""
    _db = TableauDatabase(session=db)

    # 使用统一的权限验证函数
    verify_connection_access(conn_id, current_user, db)

    # 优先从 tableau_connections 删除
    deleted = _db.delete_connection(conn_id)

    # 若 tableau_connections 中不存在（虚拟连接 ID = 10000 + mcp_server.id），
    # 则从 mcp_servers 表删除
    if not deleted and conn_id > 10000:
        from services.mcp.models import McpServer
        mcp_id = conn_id - 10000
        mcp = db.query(McpServer).filter(McpServer.id == mcp_id).first()
        if mcp and mcp.type == "tableau":
            db.delete(mcp)
            db.commit()
            deleted = True

    if not deleted:
        raise HTTPException(status_code=404, detail="连接不存在")

    log_action(current_user["id"], current_user.get("username", ""), "delete", "tableau_connection", conn_id)
    return {"message": "连接已删除"}


