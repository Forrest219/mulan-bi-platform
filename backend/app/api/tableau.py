def _field_metadata_value(field, *keys):
    metadata = field.metadata_json or {}
    if not isinstance(metadata, dict):
        return None
    for key in keys:
        if key in metadata:
            return metadata[key]
    return None


def _clean_json_value(value: Any) -> Any:
    if isinstance(value, (str, int, float, bool)) or value is None:
        return value
    if isinstance(value, (list, dict)):
        return value
    return None


def _field_mcp_payload(field) -> dict | None:
    metadata = getattr(field, "metadata_json", None) or {}
    if not isinstance(metadata, dict):
        metadata = {}
    payload = {
        "name": metadata.get("name"),
        "dataType": metadata.get("dataType"),
        "role": metadata.get("role"),
        "columnClass": metadata.get("columnClass"),
        "logicalTableId": metadata.get("logicalTableId"),
        "dataCategory": metadata.get("dataCategory"),
        "defaultAggregation": metadata.get("defaultAggregation"),
        "defaultFormat": metadata.get("defaultFormat"),
        "formula": metadata.get("formula"),
        "isAutoGenerated": metadata.get("isAutoGenerated"),
        "hasUserReference": metadata.get("hasUserReference"),
        "isHidden": metadata.get("isHidden"),
        "fullyQualifiedName": metadata.get("fullyQualifiedName"),
    }
    _mcp_exclusive = {'columnClass', 'logicalTableId', 'dataCategory', 'defaultAggregation',
                      'defaultFormat', 'formula', 'isAutoGenerated', 'hasUserReference'}
    return payload if any(payload.get(k) is not None for k in _mcp_exclusive) else None


def _datasource_field_api_payload(field, *, source: str = "mcp_cache") -> dict:
    description = _clean_json_value(getattr(field, "description", None)) or ""
    ai_description = _clean_json_value(getattr(field, "ai_description", None))
    mcp_queryable = getattr(field, "mcp_queryable", None)
    return {
        "name": _clean_json_value(getattr(field, "field_name", None)) or "",
        "field": _clean_json_value(getattr(field, "field_name", None)) or "",
        "caption": _clean_json_value(getattr(field, "field_caption", None)) or "",
        "data_type": _clean_json_value(getattr(field, "data_type", None)) or "STRING",
        "role": _clean_json_value(getattr(field, "role", None)) or "dimension",
        "description": description,
        "meaning": ai_description or description,
        "aggregation": _clean_json_value(getattr(field, "aggregation", None)),
        "formula": _clean_json_value(getattr(field, "formula", None)),
        "is_calculated": bool(_clean_json_value(getattr(field, "is_calculated", None))),
        "source": source,
        "fetched_at": _format_tableau_dt(getattr(field, "fetched_at", None)),
        "mcp_queryable": mcp_queryable if isinstance(mcp_queryable, bool) else None,
        "mcp_field_name": _clean_json_value(getattr(field, "mcp_field_name", None)),
        "mcp_field_caption": _clean_json_value(getattr(field, "mcp_field_caption", None)),
        "mcp_checked_at": _format_tableau_dt(getattr(field, "mcp_checked_at", None)),
        "mcp_last_error": _clean_json_value(getattr(field, "mcp_last_error", None)),
        "queryability_status": queryability_status(field),
        "mcp": _field_mcp_payload(field),
    }


def _fields_response_payload(fields, *, cache_status: str, source: str = "mcp_cache", **extra) -> dict:
    field_list = list(fields or [])
    payload = {
        "total": len(field_list),
        "cache_status": cache_status,
        "fields": [_datasource_field_api_payload(f, source=source) for f in field_list],
    }
    payload.update(summarize_field_capabilities(field_list))
    payload.update(extra)
    return payload


