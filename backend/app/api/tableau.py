    # 非 admin 且未指定 connection_id 时，强制按 owner_id 过滤（IDOR 防护）
    owner_id_filter: Optional[int] = None
    if user["role"] != "admin" and connection_id is None:
        owner_id_filter = user["id"]

    assets, total = _db.search_assets(
        connection_id=connection_id,
        query=q,
        asset_type=asset_type,
        page=page,
        page_size=page_size,
        owner_id=owner_id_filter,
    )

    # 批量加载连接信息，为每个资产注入 server_url / site（供前端构造 Tableau 跳转链接）
    from services.tableau.models import TableauConnection as _TC
    conn_ids = list({a.connection_id for a in assets})
    conn_map = {}
    if conn_ids:
        conns = db.query(_TC).filter(_TC.id.in_(conn_ids)).all()
        conn_map = {c.id: c for c in conns}

    asset_dicts = []
    for a in assets:
        d = a.to_dict()
        conn = conn_map.get(a.connection_id)
        if conn:
            d['server_url'] = conn.server_url
            d['site'] = conn.site or ''
        asset_dicts.append(d)

    return {
        "assets": asset_dicts,
        "total": total,
        "page": page,
        "page_size": page_size
    }


